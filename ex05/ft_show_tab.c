#include <unistd.h>
#include "ft_stock_str.h"

void	ft_putstr(char *str)
{
	while (*str)
		write(1, str++, 1);
}

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		nb = 147483648;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		nb *= -1;
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
	}
	ft_putchar(nb % 10 + '0');
}


void	ft_show_tab(t_stock_str *list)
{
	while (list->str)
	{
		ft_putstr(list->str);
		ft_putchar('\n');
		ft_putnbr(list->size);
		ft_putchar('\n');
		ft_putstr(list->copy);
		ft_putchar('\n');
		list++;
	}
}
